input_number=input("Enter a number: ")
input_number=int(input_number)
if(input_number>0):
    print("The number is positive")
else:
    print("The number is negative")
if(input_number%2==0):
    print("The number is even")
else:
    print("The number is odd")
if(input_number==0):
    print("The number is zero")
