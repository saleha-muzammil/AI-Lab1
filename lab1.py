def average_calculator():

    print("Enter numbers one by one. Type 'done' when you are finished.")
    numbers = []

    while True:
        user_input = input("Enter a number (or 'done' to finish): ").strip()

        if user_input.lower() == 'done':
            break

        try:
            number = float(user_input)
            numbers.append(number)
        except ValueError:
            print("Invalid input. Please enter a valid number.")

    if not numbers:
        print("No numbers were entered. Cannot calculate an average.")
        return


    average = sum(numbers) / len(numbers)
    print(f"The average of the entered numbers is: {average:.2f}")


average_calculator()

