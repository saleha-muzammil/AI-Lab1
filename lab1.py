def fibonacci_sequence(n):
    if n <= 0:
        return []
    elif n == 1:
        return [0]
    elif n == 2:
        return [0, 1]
    else:
        sequence = [0, 1]
        for i in range(2, n):
            next_number = sequence[-1] + sequence[-2]
            sequence.append(next_number)
        return sequence

num_fibonacci = int(input("How many Fibonacci numbers do you want to generate? "))
result = fibonacci_sequence(num_fibonacci)
print(f"The first {num_fibonacci} Fibonacci numbers are: {', '.join(map(str, result))}")

