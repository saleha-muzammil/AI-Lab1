def get_average():
    nums = []

    while True:
        val = input("Enter a number (or type 'done' to finish): ")

        if val.lower() == 'done':
            break

        try:
            num = float(val)
            nums.append(num)
        except ValueError:
            print("Invalid input. Please enter a valid number.")

    if nums:
        avg = sum(nums) / len(nums)
        print(f"The average is: {avg}")
    else:
        print("No numbers were entered.")

get_average()

