def remove_dup(nums):
    seen = set()
    res = []
    for n in nums:
        if n not in seen:
            seen.add(n)
            res.append(n)
    return res

nums = [1, 2, 3, 4, 4, 5]
result = remove_dup(nums)
print(result)

