def count_words():
    s = input("Enter words separated by spaces: ").split()
    d = {}

    for w in s:
        if w in d:
            d[w] += 1
        else:
            d[w] = 1

    return d

result = count_words()
print("Word frequency:")
for k, v in result.items():
    print(f"{k}: {v}")

